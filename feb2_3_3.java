package feb_2;
import java.util.*;
public class feb2_3_3 {
	public static void main(String[] args) {
		Scanner s=new Scanner(System.in);
		System.out.println("enter a number in multiples of 10");
		int number=s.nextInt();
		if(number==10) 
			System.out.println("checked given number is 10");
		else if(number==20)
			System.out.println("checked given number is 20");
		else if(number==30)
			System.out.println("checked given number is 30");
		else if(number==40)
			System.out.println("checked given number is 40");
		else if(number==50)
			System.out.println("checked given number is 50");
		else
		System.out.println("checked given number is not in the list");
		
	}

}
